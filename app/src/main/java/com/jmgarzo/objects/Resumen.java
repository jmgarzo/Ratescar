public class Resumen {

    private int icono;
    private String titulo, subtitulo, mediaConsumo, kmRecorridos, numeroRepostajes, litrosConsumidos, gastoCombutible, gastoMantenimiento, gastoTotal, precioKm;

    public int getIcono() {
        return icono;
    }

    public void setIcono(int icono) {
        this.icono = icono;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getSubtitulo() {
        return subtitulo;
    }

    public void setSubtitulo(String subtitulo) {
        this.subtitulo = subtitulo;
    }

    public String getMediaConsumo() {
        return mediaConsumo;
    }

    public void setMediaConsumo(String mediaConsumo) {
        this.mediaConsumo = mediaConsumo;
    }

    public String getKmRecorridos() {
        return kmRecorridos;
    }

    public void setKmRecorridos(String kmRecorridos) {
        this.kmRecorridos = kmRecorridos;
    }

    public String getNumeroRepostajes() {
        return numeroRepostajes;
    }

    public void setNumeroRepostajes(String numeroRepostajes) {
        this.numeroRepostajes = numeroRepostajes;
    }

    public String getLitrosConsumidos() {
        return litrosConsumidos;
    }

    public void setLitrosConsumidos(String litrosConsumidos) {
        this.litrosConsumidos = litrosConsumidos;
    }

    public String getGastoCombutible() {
        return gastoCombutible;
    }

    public void setGastoCombutible(String gastoCombutible) {
        this.gastoCombutible = gastoCombutible;
    }

    public String getGastoMantenimiento() {
        return gastoMantenimiento;
    }

    public void setGastoMantenimiento(String gastoMantenimiento) {
        this.gastoMantenimiento = gastoMantenimiento;
    }

    public String getGastoTotal() {
        return gastoTotal;
    }

    public void setGastoTotal(String gastoTotal) {
        this.gastoTotal = gastoTotal;
    }

    public String getPrecioKm() {
        return precioKm;
    }

    public void setPrecioKm(String precioKm) {
        this.precioKm = precioKm;
    }
}
